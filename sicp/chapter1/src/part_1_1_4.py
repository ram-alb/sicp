def square(num):
    """
    Square a number.

    Args:
        num: number

    Returns:
        number
    """
    return num * num


def sum_of_squares(num1, num2):
    """
    Sum of squares of two numbers.

    Args:
        num1: number
        num2: number

    Returns:
        number
    """
    return square(num1) + square(num2)
